<template>
    <div class="login-box">
        <h2>Update</h2>
        <form>
            <div class="user-box">
                <label>Name_mesa</label>
                <input type="text" name="" required="" v-model="state.mesa.name_mesa">
            </div>
            <div class="user-box">
                <label>Capacity</label>
                <input type="number" name="" required="" v-model="state.mesa.capacity">
            </div>
            <div class="user-box">
                <label>Photo</label>
                <input type="url" name="" required="" v-model="state.mesa.photo">
            </div>
            <a @click="createSubmit()" v-if="!isUpdate">
                <span></span>
                <span></span>
                <span></span>
                <span></span>
                Create
            </a>
            <a @click="editSubmit()" v-if="isUpdate">
                <span></span>
                <span></span>
                <span></span>
                <span></span>
                Update
            </a>
            <a @click="cancel()">
                <span></span>
                <span></span>
                <span></span>
                <span></span>
                Cancel
            </a>
        </form>
    </div>
</template>

<script>
import { reactive, getCurrentInstance } from 'vue'
import { useRouter } from 'vue-router';
export default {
    props: {
        mesa: Object
    },
    emits: {
        data: Object
    },
    computed: {
        isUpdate() {
            const path = this.$route.path.split('/');
            return path[3] == 'update';
        },
    },
    setup(props) {
        const router = useRouter();
        const mesa = props.mesa;
        const { emit } = getCurrentInstance();

        const state = reactive({
            mesa: { ...mesa }
        });

        const createSubmit = () => {
            emit('data', state.mesa)
        }

        const editSubmit = () => {
            emit('data', state.mesa)
        }
        const cancel = () => {
            router.push({ name: "mesasList" })
        }
        return { state, editSubmit, cancel, createSubmit }
    }
}
</script>

